public class Ctenar {
    private final Integer id;
    private final String jmeno;

    public Ctenar(Integer id, String jmeno) {
        this.id = id;
        this.jmeno = jmeno;
    }

    public Integer getId() {
        return id;
    }

    public String getJmeno() {
        return jmeno;
    }
}